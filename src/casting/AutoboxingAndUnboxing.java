package casting;

public class AutoboxingAndUnboxing {
    public static void main(String[] args) {
        //Autoboxing (primitive ->object [wrapper class]) its always implicit
        float num = 45.6F;
        float numFloat = num;

        //Unboxing (object[wrapper class]  -> primitive //its always implicit
        Character character = 'D';
        char characterPrimitive = character;



    }
}
