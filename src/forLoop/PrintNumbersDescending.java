package forLoop;

public class PrintNumbersDescending {
    public static void main(String[] args) {
        for(int i = 100; i >= 0; i--) { //post-decrement  //int i = 100 ; i > -1; i--
            System.out.println(i);
            //start point is 100
            //end point is 0
            // update is decrement
    }
}}
